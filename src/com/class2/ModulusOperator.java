package com.class2;

public class ModulusOperator {
	public static void main(String[] args) {
		
		float n1=12.3f;
		float n2=3.4f;
		float div=n1/n2;
		
		System.out.println(div);
		
		int num1=13;
		int num2=2;
		double div1=num1/num2;
		System.out.println(div1);
		
		int a=15;
		int b=4;
		
		int remainder=a%b;
		System.out.println(remainder);
		
	}

}
