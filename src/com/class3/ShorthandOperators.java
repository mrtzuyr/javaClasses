package com.class3;

public class ShorthandOperators {

	public static void main(String[] args) {
		
		int num=1000;
		num=num+1000;
		System.out.println(num);
		
		int num4=50;
		num4+=20; //num4=num4+20
		num4-=30; //num4=70-30
		System.out.println(num4);
		
		num4/=5;
		System.out.println(num4);
		num4%=6;
		System.out.println(num4);
		
		

	}

}
